
#* Built-in Math Functions
x = min(5, 10, 25)      #min
y = max(5, 10, 25)      #max
print(x)
print(y)

x = abs(-7.25)          #absolute
print(x)

x = pow(4, 3)           #power
print(x)

#* The Math Module
import math

x = math.sqrt(64)       #square root
print(x)

x = math.ceil(1.4)      #upwards to its nearest integer
y = math.floor(1.4)     #downwards to its nearest integer
print(x) # returns 2
print(y) # returns 1

x = math.pi
print(x)
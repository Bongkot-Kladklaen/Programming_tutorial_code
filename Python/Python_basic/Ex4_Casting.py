
#* Casting anyting to int()
x = int(1)   # 1 is int -> x will be 1
y = int(2.8) # 2.8 is float -> y will be 2
z = int("3") # 3 is string -> z will be 3

#* Casting anyting to float()
x = float(1)     # 1 is int -> x will be 1.0
y = float(2.8)   # 2.8 is float -> y will be 2.8
z = float("3")   # 3 is string -> z will be 3.0
w = float("4.2") # 4.2 is string -> w will be 4.2

#* Casting anyting to str()
x = str("s1") # s1 is string -> x will be 's1'
y = str(2)    # 2 is int -> y will be '2'
z = str(3.0)  # 3.0 is float -> z will be '3.0'